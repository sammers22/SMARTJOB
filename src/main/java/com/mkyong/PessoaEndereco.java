package com.mkyong;

public class PessoaEndereco {

	private Integer numPessoa;
	private Integer numEndereco;
	private Integer codTpEndereco;
	private Integer codFinEndereco;
	
	public Integer getNumPessoa() {
		return numPessoa;
	}
	public void setNumPessoa(Integer numPessoa) {
		this.numPessoa = numPessoa;
	}
	public Integer getNumEndereco() {
		return numEndereco;
	}
	public void setNumEndereco(Integer numEndereco) {
		this.numEndereco = numEndereco;
	}
	public Integer getCodTpEndereco() {
		return codTpEndereco;
	}
	public void setCodTpEndereco(Integer codTpEndereco) {
		this.codTpEndereco = codTpEndereco;
	}
	public Integer getCodFinEndereco() {
		return codFinEndereco;
	}
	public void setCodFinEndereco(Integer codFinEndereco) {
		this.codFinEndereco = codFinEndereco;
	}

	
	
}
